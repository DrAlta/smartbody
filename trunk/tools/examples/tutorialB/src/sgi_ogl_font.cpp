void sgiGLFont::init( void )	{
	int alignment;
    GLuint i;
    
	if( font_handle == 0 ) {
		glGetIntegerv( GL_UNPACK_ALIGNMENT, &alignment );
		glPixelStorei( GL_UNPACK_ALIGNMENT, 1 );

	    font_handle = glGenLists( 128 );
	    for( i = 32; i < 127; i++ ) {
			glNewList( font_handle + i, GL_COMPILE );
				glBitmap( 
					SGI_GLFONT_CHAR_WID, SGI_GLFONT_CHAR_HGT, 
					0.0, 2.0, 
					(GLfloat)SGI_GLFONT_SPACE_WID, 0.0, 
					G_sgi_ogl_font_rasters[ i - 32 ] 
				);
			glEndList();
		}

		glPixelStorei( GL_UNPACK_ALIGNMENT, alignment );
	}
}

void sgiGLFont::print( char *s )	{   
	
	if( font_handle == 0 )	{
		init();
	}
	
	glPushAttrib( GL_LIST_BIT );
	    glListBase( font_handle );
	    glCallLists( (GLsizei)strlen( s ), GL_UNSIGNED_BYTE, (GLubyte*) s );
	glPopAttrib();
}

